#include <stdio.h>

int main(){
    /*
    Incremento (++)
    Pré-incremento (++a)
    Pós-incremento (a++)
    Decremento (--)
    Pré-decremento (--a)
    Pós-decremento (a--)
    */

    int numero1 = 1;
    printf("antes do incremento: %d\n", numero1); 

    numero1++;

    printf("após o incremento: %d\n", numero1);       

    numero1--; 

    printf("após o decremento: %d\n", numero1);




}